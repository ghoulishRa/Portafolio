void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	//envio por serial
	if (htim == &htim6)
	{

		HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
		pwm = (resolution_adc[0]*100)/255;
		//voltaje[0] = (resolution_adc[0]/255.0)*3.3;
		rpm = (1/temporal)*(60000000.0/34.0*pulsos);
		TIM4->CCR4 = pwm;

		formato_uart = sprintf(uart_buf,"resolution:%d\t rpm:%.2f\t pwm:%.2 \r\n",resolution_adc[0],rpm,pwm);
		HAL_UART_Transmit(&huart3, (uint8_t *)uart_buf, formato_uart,100);
	}

}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if (GPIO_Pin == GPIO_PIN_0)
	{
		HAL_GPIO_TogglePin(LD1_GPIO_Port, LD1_Pin);
		temporal = __HAL_TIM_GET_COUNTER(&htim2) - val_actual;
		val_actual = __HAL_TIM_GET_COUNTER(&htim2);
	}

}




void swap(float *xp, float *yp)
{
    float temp = *xp;
    *xp = *yp;
    *yp = temp;
}

void selection_sort (float arr[], int n)
{
	int i, j, ancla;

	    for (i = 0; i < n-1; i++)
	    {
	        ancla = i;
	        for (j = i+1; j < n; j++)
	        {
	        	if (arr[j] < arr[ancla])
	        		ancla = j;

	        }
	        if(ancla != i)
	        swap(&arr[ancla], &arr[i]);
	    }
}


void Bubble_sort(float arr[], int n) {
    for (int i = 0; i < n-1; i++)
    {
        for (int j = 0; j < n-i-1; j++)
        {
            if (arr[j] > arr[j+1])
            {
            	float temp = arr[j];
            	arr[j] = arr[j+1];
            	arr[j+1] = temp;
            }
        }
    }
}

void merge(float arr[], int izq, int mid, int der)
{
    int i, j, k;
    int n1 = mid - izq + 1;
    int n2 = der - mid;

    float IZQ[n1];
	float DER[n2];

    for (i = 0; i < n1; i++)
        IZQ[i] = arr[izq + i];
    for (j = 0; j < n2; j++)
        DER[j] = arr[mid + 1 + j];

    i = 0;
    j = 0;
    k = izq;
    while (i < n1 && j < n2)
    {
        if (IZQ[i] <= DER[j])
        {
            arr[k] = IZQ[i];
            i++;
        }
        else
        {
            arr[k] = DER[j];
            j++;
        }
        k++;
    }
    while (i < n1)
    {
        arr[k] = IZQ[i];
        i++;
        k++;
    }
    while (j < n2)
    {
        arr[k] = DER[j];
        j++;
        k++;
    }
}

void merge_sort (float arr[], int izq, int der)
{
	if (izq < der)
	{
		int mid = izq + (der - izq) / 2;
	    merge_sort(arr, izq, mid);
	    merge_sort(arr, mid + 1, der);
	    merge(arr, izq, mid, der);
	}
}

int partition(float arr[], int low, int high)
{

    float pivot = arr[high];
    int i = (low - 1);

    for (int j = low; j <= high - 1; j++){
        if (arr[j] < pivot)
        {
            i++;
            swap(&arr[i], &arr[j]);
        }
    }
    swap(&arr[i + 1], &arr[high]);
    return (i + 1);
}



void Quick_Sort(float arr[], int low, int high)
{
    if (low < high)
    {
    	int pi = partition(arr, low, high);
        Quick_Sort(arr, low, pi - 1);
        Quick_Sort(arr, pi + 1, high);
    }
}

void Insertion_Sort(float arr[], int n) {
    int i, j;
    float key;
    for (i = 1; i < n; i++)
    {
        key = arr[i];
        j = i - 1;

        while (j >= 0 && arr[j] > key)
        {
        	arr[j + 1] = arr[j];
            j = j - 1;
        }
        arr[j + 1] = key;
    }
}

int Linear_Search(float arr[], int tam, float valor) {
    for (int i = 0; i < tam; i++)
    {
        if (arr[i] == valor)
        {
            return i;
        }
    }
    return -1;
}

int binary_search(float arr[], int tam, float valor) {
    int izquierda = 0;
    int derecha = tam - 1;

    while (izquierda <= derecha)
    {
        int medio = izquierda + (derecha - izquierda) / 2;

        if (arr[medio] == valor)
        {
            return medio;
        }
        if (arr[medio] < valor)
        {
            izquierda = medio + 1;
        }
        else
        {
            derecha = medio - 1;
        }
    }
    return -1;
}


